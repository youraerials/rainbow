/**
 * Render `config/rainbow.yaml` from the wizard's collected state.
 *
 * The schema is small and stable so a string template is cleaner than
 * pulling in a full YAML library — no npm dep, no surprises with quoting
 * conventions. The orchestrator + generate-config.sh consume this file
 * via `yq`, which tolerates either explicitly-quoted strings or bare
 * tokens, so we don't need to be precious about quotes.
 */

import { SetupState } from "./state.js";

export interface RenderInput {
    state: SetupState;
    /** rainbow.rocks zone ID (operator's). */
    zoneId: string;
    /** Cloudflare Tunnel ID returned by the Worker. */
    tunnelId: string;
}

const SERVICE_DEFAULTS = [
    "photos",
    "mail",
    "webmail",
    "files",
    "docs",
    "media",
] as const;

export function renderRainbowYaml(input: RenderInput): string {
    const { state, zoneId, tunnelId } = input;
    const prefix = state.domain?.prefix ?? "";
    const zone = state.domain?.zone ?? "rainbow.rocks";
    const adminEmail = state.admin?.email ?? "";
    const adminName = state.admin?.name ?? "";
    const services = state.services ?? {};

    const enabled = (key: string): boolean =>
        services[key] ?? SERVICE_DEFAULTS.includes(key as typeof SERVICE_DEFAULTS[number]);

    return `# Rainbow Configuration
# Auto-generated by the setup wizard. Edit and re-run \`make config\` to
# apply changes; or use the dashboard's Settings page for individual
# fields.

rainbow:
  version: "0.1.0"

domain:
  prefix: "${prefix}"
  zone: "${zone}"

cloudflare:
  zone_id: "${zoneId}"
  tunnel_id: "${tunnelId}"
  # api_token is read from macOS Keychain at runtime, not stored here

admin:
  name: "${escapeYaml(adminName)}"
  email: "${adminEmail}"

services:
  postgres:
    version: "17"
  valkey:
    version: "8"
  caddy:
    http_port: 80
    https_port: 443
  cloudflared:
    metrics_port: 2000
  authentik:
    enabled: true
  immich:
    enabled: ${boolStr(enabled("photos"))}
    upload_path: "~/Library/Application Support/Rainbow/immich/upload"
    enable_ml: true
  stalwart:
    enabled: ${boolStr(enabled("mail"))}
    data_path: "~/Library/Application Support/Rainbow/stalwart"
    domains:
      - ${prefix ? `${prefix}-mail.${zone}` : `mail.${zone}`}
  cryptpad:
    enabled: ${boolStr(enabled("docs"))}
  seafile:
    enabled: ${boolStr(enabled("files"))}
    data_path: "~/Library/Application Support/Rainbow/seafile"
  jellyfin:
    enabled: ${boolStr(enabled("media"))}
    media_paths:
      - "~/Movies"
      - "~/Music"
  webmail:
    enabled: ${boolStr(enabled("webmail"))}
  minecraft:
    enabled: false
    memory: "4G"
`;
}

function boolStr(v: boolean): string {
    return v ? "true" : "false";
}

function escapeYaml(s: string): string {
    return s.replace(/"/g, '\\"');
}
