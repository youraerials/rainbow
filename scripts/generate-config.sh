#!/usr/bin/env bash
#
# generate-config.sh — Reads config/rainbow.yaml and produces per-service configs.
# Usage: ./scripts/generate-config.sh [config-file]

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
CONFIG_FILE="${1:-$PROJECT_ROOT/config/rainbow.yaml}"
DEFAULTS_FILE="$PROJECT_ROOT/config/defaults.yaml"
TEMPLATES_DIR="$PROJECT_ROOT/config/templates"
INFRA_DIR="$PROJECT_ROOT/infrastructure"

# ─── Dependency check ────────────────────────────────────────────
check_deps() {
    local missing=()
    for cmd in yq sed; do
        if ! command -v "$cmd" &>/dev/null; then
            missing+=("$cmd")
        fi
    done
    if [ ${#missing[@]} -gt 0 ]; then
        echo "ERROR: Missing required tools: ${missing[*]}" >&2
        echo "Install with: brew install ${missing[*]}" >&2
        exit 1
    fi
}

# ─── Read a value from config, falling back to defaults ──────────
cfg() {
    local key="$1"
    local val
    val=$(yq eval "$key" "$CONFIG_FILE" 2>/dev/null)
    if [ "$val" = "null" ] || [ -z "$val" ]; then
        val=$(yq eval "$key" "$DEFAULTS_FILE" 2>/dev/null)
    fi
    echo "$val"
}

# ─── Expand a leading ~ to $HOME (filesystem paths only) ─────────
expand_path() {
    local path="$1"
    echo "${path/#\~/$HOME}"
}

# ─── Read a secret from macOS Keychain ───────────────────────────
keychain_get() {
    local service="$1"
    security find-generic-password -s "rainbow-$service" -w 2>/dev/null || echo ""
}

# ─── Read OAuth client credentials from Keychain ─────────────────
oauth_client_id() {
    local slug="$1"
    security find-generic-password -s "rainbow-oauth-${slug}-client-id" -w 2>/dev/null || echo ""
}

oauth_client_secret() {
    local slug="$1"
    security find-generic-password -s "rainbow-oauth-${slug}-client-secret" -w 2>/dev/null || echo ""
}

# ─── Template substitution ───────────────────────────────────────
render_template() {
    local template="$1"
    local output="$2"
    shift 2

    mkdir -p "$(dirname "$output")"
    cp "$template" "$output"
    while [ $# -ge 2 ]; do
        local key="$1"
        local value="$2"
        sed -i '' "s|{{${key}}}|${value}|g" "$output"
        shift 2
    done

    echo "  Generated: $output"
}

# ─── Check if a service is enabled ───────────────────────────────
service_enabled() {
    local service="$1"
    local enabled
    enabled=$(cfg ".services.${service}.enabled")
    [ "$enabled" != "false" ]
}

# ─── Main ────────────────────────────────────────────────────────
main() {
    check_deps

    echo "Generating Rainbow configuration..."
    echo "  Config: $CONFIG_FILE"
    echo ""

    # ── Core values ──────────────────────────────────────────────
    local prefix zone host_prefix web_host admin_email pg_user tunnel_id
    prefix=$(cfg '.domain.prefix')
    zone=$(cfg '.domain.zone')
    [ "$prefix" = "null" ] && prefix=""
    [ "$zone" = "null" ] && zone=""
    if [ -n "$prefix" ]; then
        host_prefix="${prefix}-"
        web_host="${prefix}.${zone}"
    else
        host_prefix=""
        web_host="$zone"
    fi
    # `domain` is kept as an alias for templates that still reference
    # RAINBOW_DOMAIN; it equals the web host (the user-facing apex).
    local domain="$web_host"
    admin_email=$(cfg '.admin.email')
    pg_user=$(cfg '.services.postgres.user // "rainbow"')
    tunnel_id=$(cfg '.cloudflare.tunnel_id')

    # ── Service paths ────────────────────────────────────────────
    local stalwart_data immich_upload seafile_data
    stalwart_data=$(expand_path "$(cfg '.services.stalwart.data_path')")
    immich_upload=$(expand_path "$(cfg '.services.immich.upload_path')")
    seafile_data=$(expand_path "$(cfg '.services.seafile.data_path')")

    # ── Secrets from Keychain ────────────────────────────────────
    local pg_password mariadb_root_pass authentik_secret authentik_bootstrap_pass
    local seafile_admin_pass cf_tunnel_token
    pg_password=$(keychain_get "postgres-password")
    mariadb_root_pass=$(keychain_get "mariadb-root-password")
    authentik_secret=$(keychain_get "authentik-secret")
    authentik_bootstrap_pass=$(keychain_get "authentik-bootstrap-password")
    seafile_admin_pass=$(keychain_get "seafile-admin-password")
    cf_tunnel_token=$(keychain_get "cloudflare-tunnel-token")

    # ML enabled
    local immich_ml_enabled
    immich_ml_enabled=$(cfg '.services.immich.enable_ml // "true"')

    # OAuth credentials (populated after setup-providers.sh)
    local oauth_photos_id oauth_photos_secret
    local oauth_files_id oauth_files_secret
    oauth_photos_id=$(oauth_client_id "photos")
    oauth_photos_secret=$(oauth_client_secret "photos")
    oauth_files_id=$(oauth_client_id "files")
    oauth_files_secret=$(oauth_client_secret "files")

    echo "Core:"

    # ── Docker Compose .env ──────────────────────────────────────
    cat > "$INFRA_DIR/.env" <<EOF
# Auto-generated by generate-config.sh — do not edit
RAINBOW_HOST_PREFIX=${host_prefix}
RAINBOW_ZONE=${zone}
RAINBOW_WEB_HOST=${web_host}
RAINBOW_DOMAIN=${domain}
POSTGRES_USER=${pg_user}
POSTGRES_PASSWORD=${pg_password}
MARIADB_ROOT_PASSWORD=${mariadb_root_pass}
AUTHENTIK_SECRET_KEY=${authentik_secret}
AUTHENTIK_BOOTSTRAP_PASSWORD=${authentik_bootstrap_pass}
AUTHENTIK_BOOTSTRAP_EMAIL=${admin_email}
IMMICH_UPLOAD_PATH=${immich_upload}
IMMICH_MACHINE_LEARNING_ENABLED=${immich_ml_enabled}
SEAFILE_ADMIN_EMAIL=${admin_email}
SEAFILE_ADMIN_PASSWORD=${seafile_admin_pass}
SEAFILE_DATA_PATH=${seafile_data}
CLOUDFLARE_TUNNEL_TOKEN=${cf_tunnel_token}
EOF
    echo "  Generated: $INFRA_DIR/.env"

    # ── Caddyfile ────────────────────────────────────────────────
    render_template \
        "$TEMPLATES_DIR/caddy/Caddyfile.j2" \
        "$INFRA_DIR/caddy/Caddyfile" \
        "HOST_PREFIX" "$host_prefix" \
        "ZONE" "$zone" \
        "WEB_HOST" "$web_host" \
        "RAINBOW_DOMAIN" "$domain"

    # ── Cloudflare Tunnel ────────────────────────────────────────
    render_template \
        "$TEMPLATES_DIR/cloudflared/config.yml.j2" \
        "$INFRA_DIR/cloudflared/config.yml" \
        "HOST_PREFIX" "$host_prefix" \
        "ZONE" "$zone" \
        "WEB_HOST" "$web_host" \
        "CLOUDFLARE_TUNNEL_ID" "$tunnel_id"

    # ── PostgreSQL ───────────────────────────────────────────────
    render_template \
        "$TEMPLATES_DIR/postgres/init.sql.j2" \
        "$INFRA_DIR/postgres/init/00-create-databases.sql" \
        "POSTGRES_USER" "$pg_user"

    # ── Per-service configs ──────────────────────────────────────

    echo ""
    echo "Services:"

    # Authentik
    if service_enabled "authentik"; then
        render_template \
            "$TEMPLATES_DIR/authentik/.env.j2" \
            "$INFRA_DIR/authentik/.env" \
            "AUTHENTIK_SECRET_KEY" "$authentik_secret" \
            "AUTHENTIK_BOOTSTRAP_PASSWORD" "$authentik_bootstrap_pass" \
            "ADMIN_EMAIL" "$admin_email" \
            "POSTGRES_USER" "$pg_user" \
            "POSTGRES_PASSWORD" "$pg_password" \
            "RAINBOW_DOMAIN" "$domain"
    fi

    # Immich
    if service_enabled "immich"; then
        render_template \
            "$TEMPLATES_DIR/immich/.env.j2" \
            "$INFRA_DIR/immich/.env" \
            "POSTGRES_USER" "$pg_user" \
            "POSTGRES_PASSWORD" "$pg_password" \
            "IMMICH_ML_ENABLED" "$immich_ml_enabled" \
            "HOST_PREFIX" "$host_prefix" \
            "ZONE" "$zone" \
            "OAUTH_PHOTOS_CLIENT_ID" "$oauth_photos_id" \
            "OAUTH_PHOTOS_CLIENT_SECRET" "$oauth_photos_secret"
    fi

    # Stalwart: nothing to render. The orchestrator passes
    # `--config /opt/stalwart/etc/config.json`, which doesn't exist on first
    # start so Stalwart enters bootstrap mode. The user completes the web
    # wizard once (services/stalwart/README.md), and Stalwart writes its
    # canonical config.json into the bind-mounted /opt/stalwart/etc/. Pre-
    # rendering anything here just gets ignored at best and breaks bootstrap
    # at worst — Stalwart 0.16's per-store schema is strict.
    if service_enabled "stalwart"; then
        mkdir -p "$stalwart_data/etc" 2>/dev/null || true
    fi

    # Seafile
    if service_enabled "seafile"; then
        render_template \
            "$TEMPLATES_DIR/seafile/.env.j2" \
            "$INFRA_DIR/seafile/.env" \
            "MARIADB_ROOT_PASSWORD" "$mariadb_root_pass" \
            "ADMIN_EMAIL" "$admin_email" \
            "SEAFILE_ADMIN_PASSWORD" "$seafile_admin_pass" \
            "HOST_PREFIX" "$host_prefix" \
            "ZONE" "$zone"

        render_template \
            "$TEMPLATES_DIR/seafile/seahub_settings_extra.py.j2" \
            "$INFRA_DIR/seafile/seahub_settings_extra.py" \
            "HOST_PREFIX" "$host_prefix" \
            "ZONE" "$zone" \
            "RAINBOW_DOMAIN" "$domain" \
            "OAUTH_FILES_CLIENT_ID" "$oauth_files_id" \
            "OAUTH_FILES_CLIENT_SECRET" "$oauth_files_secret"
    fi

    # CryptPad
    if service_enabled "cryptpad"; then
        local cryptpad_admin_key
        cryptpad_admin_key=$(keychain_get "cryptpad-admin-key")
        render_template \
            "$TEMPLATES_DIR/cryptpad/config.js.j2" \
            "$INFRA_DIR/cryptpad/customize/config.js" \
            "HOST_PREFIX" "$host_prefix" \
            "ZONE" "$zone" \
            "CRYPTPAD_ADMIN_KEY" "$cryptpad_admin_key"
    fi

    # Minecraft
    if service_enabled "minecraft"; then
        local mc_name mc_players mc_rcon_pass mc_dir
        mc_name=$(cfg '.services.minecraft.server_name // "Rainbow MC"')
        mc_players=$(cfg '.services.minecraft.max_players // "20"')
        mc_rcon_pass=$(keychain_get "minecraft-rcon-password")
        mc_dir="$INFRA_DIR/minecraft/server"

        if [ ! -f "$mc_dir/server.properties" ]; then
            render_template \
                "$TEMPLATES_DIR/minecraft/server.properties.j2" \
                "$mc_dir/server.properties" \
                "MC_SERVER_NAME" "$mc_name" \
                "MC_MAX_PLAYERS" "$mc_players" \
                "MC_RCON_PASSWORD" "$mc_rcon_pass"
        else
            echo "  Skipped: $mc_dir/server.properties (already exists)"
        fi
    fi

    echo ""
    echo "Configuration generated successfully."
    echo ""
    echo "Services configured for: $domain"
    echo ""

    # Summary
    local enabled_count=0
    for svc in authentik immich stalwart seafile cryptpad jellyfin minecraft; do
        if service_enabled "$svc"; then
            enabled_count=$((enabled_count + 1))
        fi
    done
    echo "$enabled_count services enabled. Run 'rainbow start' to apply and start."
}

main "$@"
